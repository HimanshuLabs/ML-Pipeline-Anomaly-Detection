-- Project 4: ML Pipeline & Anomaly Detection
-- Batch and online prediction history tables.

CREATE TABLE IF NOT EXISTS ml.batch_predictions (
    prediction_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    batch_run_id UUID NOT NULL DEFAULT gen_random_uuid(),

    model_id UUID REFERENCES ml.model_registry(model_id) ON DELETE RESTRICT,
    model_name TEXT NOT NULL,
    model_version TEXT NOT NULL,

    dataset_snapshot_id UUID REFERENCES ml.feature_snapshots(snapshot_id) ON DELETE SET NULL,
    training_dataset_id UUID REFERENCES ml.training_datasets(training_dataset_id) ON DELETE SET NULL,
    feature_schema_version TEXT NOT NULL,

    entity_type TEXT NOT NULL,
    entity_id TEXT NOT NULL,

    source_project TEXT,
    source_table TEXT,
    source_record_id TEXT,

    score_timestamp TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    anomaly_score NUMERIC NOT NULL,
    is_anomaly BOOLEAN NOT NULL,
    threshold_used NUMERIC NOT NULL,

    feature_payload_hash TEXT,
    feature_payload JSONB NOT NULL DEFAULT '{}'::jsonb,

    inference_latency_ms NUMERIC CHECK (
        inference_latency_ms IS NULL OR inference_latency_ms >= 0
    ),

    prediction_status TEXT NOT NULL DEFAULT 'success'
        CHECK (prediction_status IN ('success', 'error', 'skipped')),

    error_message TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT batch_predictions_model_version_fk
        FOREIGN KEY (model_name, model_version)
        REFERENCES ml.model_registry(model_name, model_version)
        ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS ml.online_predictions (
    prediction_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    request_id UUID NOT NULL DEFAULT gen_random_uuid(),

    model_id UUID REFERENCES ml.model_registry(model_id) ON DELETE RESTRICT,
    model_name TEXT NOT NULL,
    model_version TEXT NOT NULL,

    feature_schema_version TEXT NOT NULL,

    entity_type TEXT NOT NULL,
    entity_id TEXT NOT NULL,

    request_timestamp TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    response_timestamp TIMESTAMPTZ,

    anomaly_score NUMERIC,
    is_anomaly BOOLEAN,
    threshold_used NUMERIC,

    drift_status TEXT NOT NULL DEFAULT 'not_evaluated'
        CHECK (drift_status IN ('not_evaluated', 'normal', 'warning', 'critical')),

    request_payload JSONB NOT NULL DEFAULT '{}'::jsonb,
    feature_payload_hash TEXT,
    feature_payload JSONB NOT NULL DEFAULT '{}'::jsonb,

    latency_ms NUMERIC CHECK (
        latency_ms IS NULL OR latency_ms >= 0
    ),

    http_status_code INTEGER CHECK (
        http_status_code IS NULL OR (http_status_code >= 100 AND http_status_code <= 599)
    ),

    prediction_status TEXT NOT NULL DEFAULT 'success'
        CHECK (prediction_status IN ('success', 'error', 'validation_failed')),

    error_message TEXT,
    client_source TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT online_predictions_response_time_chk
        CHECK (
            response_timestamp IS NULL
            OR response_timestamp >= request_timestamp
        ),

    CONSTRAINT online_predictions_success_score_chk
        CHECK (
            prediction_status <> 'success'
            OR (
                anomaly_score IS NOT NULL
                AND is_anomaly IS NOT NULL
                AND threshold_used IS NOT NULL
            )
        ),

    CONSTRAINT online_predictions_model_version_fk
        FOREIGN KEY (model_name, model_version)
        REFERENCES ml.model_registry(model_name, model_version)
        ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_batch_predictions_batch_run_id
    ON ml.batch_predictions(batch_run_id);

CREATE INDEX IF NOT EXISTS idx_batch_predictions_model_version
    ON ml.batch_predictions(model_name, model_version);

CREATE INDEX IF NOT EXISTS idx_batch_predictions_entity
    ON ml.batch_predictions(entity_type, entity_id);

CREATE INDEX IF NOT EXISTS idx_batch_predictions_score_timestamp
    ON ml.batch_predictions(score_timestamp);

CREATE INDEX IF NOT EXISTS idx_batch_predictions_is_anomaly
    ON ml.batch_predictions(is_anomaly);

CREATE INDEX IF NOT EXISTS idx_online_predictions_request_id
    ON ml.online_predictions(request_id);

CREATE INDEX IF NOT EXISTS idx_online_predictions_model_version
    ON ml.online_predictions(model_name, model_version);

CREATE INDEX IF NOT EXISTS idx_online_predictions_entity
    ON ml.online_predictions(entity_type, entity_id);

CREATE INDEX IF NOT EXISTS idx_online_predictions_request_timestamp
    ON ml.online_predictions(request_timestamp);

CREATE INDEX IF NOT EXISTS idx_online_predictions_is_anomaly
    ON ml.online_predictions(is_anomaly);

CREATE INDEX IF NOT EXISTS idx_online_predictions_drift_status
    ON ml.online_predictions(drift_status);

COMMENT ON TABLE ml.batch_predictions IS
'Stores anomaly scores generated by scheduled batch inference jobs over warehouse, mart, or feature snapshot records.';

COMMENT ON TABLE ml.online_predictions IS
'Stores anomaly scores generated by online FastAPI inference requests, including latency, request status, drift status, and model version evidence.';
